// Math객체가 제공하는 함수

// int exp(x) E의 x승
// int pow(x, y) x의 y승
// int sqrt(x) x의 제곱근

// int floor(x) 소수점 이하의 값은 버림
// int ceil(x) 소수점 이하의 값을 올림
// int round(x) 반올림

// int max(x, y) x, y 중 큰 값
// int min(x, y) x, y 중 작은 값

// sin(라디안) sin 값
// cos(라디안) cos 값
// tan(라디안) tan 값

// int abs(x) x의 절대값
// int log(x) 로그 함수
// float random() 0.0 ~ 1.0 사이의 실수형 난수

let max = Math.max(200, 500);
console.log(max); // 500

let num1 = 3.141592;
console.log(Math.floor(num1)); // 3

let num2 = -3847;
console.log(Math.abs(num2)); // 3847
